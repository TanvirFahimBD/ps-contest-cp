#include<iostream>
using namespace std;
int main(){
    int a, even = 0, odd = 0, pos = 0, neg = 0;
    for(int i=0; i<5; i++){
        cin>>a;
        if(a%2 == 0){
            even++;
        }
        if(a%2 != 0){
            odd++;
        }
        if(a>0){
            pos++;
        }
        if(a<0){
            neg++;
        }
    }
    cout<<even<<" valor(es) par(es)"<<endl;
    cout<<odd<<" valor(es) impar(es)"<<endl;
    cout<<pos<<" valor(es) positivo(s)"<<endl;
    cout<<neg<<" valor(es) negativo(s)"<<endl;
    return 0;
}
